# Función para extraer los tokens con mayor atención
def analizar_atencion_ejemplo(text, verdadero, predicho, confianza, tokenizer, model):
    """
    Analiza qué palabras fueron importantes en la predicción
    """
    # Tokenizar
    inputs = tokenizer(text, return_tensors='pt', truncation=True, max_length=512)

    # ← AGREGAR ESTA LÍNEA: Mover a GPU si el modelo está en GPU
    device = next(model.parameters()).device
    inputs = {k: v.to(device) for k, v in inputs.items()}

    # Forward pass con output_attentions
    with torch.no_grad():
        outputs = model(**inputs, output_attentions=True)

    # Obtener atención (última capa, promediada entre cabezas)
    attention = outputs.attentions[-1]  # Última capa
    attention = attention[0].mean(dim=0)  # Promediar sobre cabezas

    # Tokens
    tokens = tokenizer.convert_ids_to_tokens(inputs['input_ids'][0])

    # Atención del token [CLS] (usado para clasificación)
    cls_attention = attention[0, 1:-1]  # Excluir [CLS] y [SEP]

    # Top 10 tokens más importantes
    top_indices = torch.topk(cls_attention, min(10, len(tokens)-2)).indices

    print(f"\n🔍 ANÁLISIS DE ATENCIÓN")
    print("="*80)
    print(f"Texto: {text[:100]}...")
    print(f"Verdadero: {'POSITIVO' if verdadero==1 else 'NEGATIVO'} | "
          f"Predicho: {'POSITIVO' if predicho==1 else 'NEGATIVO'} | "
          f"Confianza: {confianza:.4f}")
    print(f"\nTop palabras más importantes en la decisión:")

    for rank, idx in enumerate(top_indices, 1):
        token = tokens[idx+1]  # +1 porque saltamos [CLS]
        weight = cls_attention[idx].item()
        print(f"  {rank:2d}. {token:20s} (peso: {weight:.4f})")

    return tokens, cls_attention

