# Función para preparar dataset Amazon
def preparar_amazon_dataset(dataset, tokenizer, max_length=256):
    """
    Prepara Amazon Polarity con el mismo formato que IMDb
    """
    # Combinar title y content
    def combinar_texto(ejemplo):
        texto = (ejemplo['title'] + " " + ejemplo['content']).strip()
        return {'text': texto, 'label': ejemplo['label']}

    # Aplicar transformación
    dataset_preparado = dataset.map(combinar_texto, remove_columns=['title', 'content'])

    # Tokenizar
    def tokenizar(ejemplos):
        return tokenizer(ejemplos['text'],
                        truncation=True,
                        padding='max_length',
                        max_length=max_length)

    dataset_tokenizado = dataset_preparado.map(
        tokenizar,
        batched=True,
        remove_columns=['text']
    )

    return dataset_tokenizado

